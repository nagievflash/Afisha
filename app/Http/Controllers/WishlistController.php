     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if (Auth::check()) {
            try {
                $user = Auth::user();
                $user->wishlist()->attach($request->input('event_id'));
                return '200';
            } catch (\Exception $e) {
                if ($e->getCode() == 23000) $user->wishlist()->detach($request->input('event_id'));
                return $e->getCode();
            }
        }
        else return '404';
    }

